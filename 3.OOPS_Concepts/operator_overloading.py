# Operator overloading in Python


# This different behaviour of a single operator for different types of operands is called Operator Overloading.

# Take a look at the following snippet:

a = 10
b = 200

print(a + b)

# here + operator is used for addition of two numbers.


c = "hello"
d = "coco"

print(c + d)

# here + operator is used for concatenation of two strings.


e = 10.99
f = 20.99

print(e + f)

# here + operator is used for addition of two floats numbers.

#  in the above snippet, we have used + operator for three different types of additions that is called operator overloading.
